namespace PulpMXFantasy.Domain.Enums;

/// <summary>
/// Represents the motorcycle class a rider competes in.
/// </summary>
/// <remarks>
/// In supercross/motocross racing, there are two main classes:
/// - 250cc class (smaller bikes, often younger or lighter riders)
/// - 450cc class (premier class with larger, more powerful bikes)
/// </remarks>
public enum BikeClass
{
    /// <summary>
    /// 250cc class - intermediate/developmental class
    /// </summary>
    Class250,

    /// <summary>
    /// 450cc class - premier/expert class
    /// </summary>
    Class450
}
